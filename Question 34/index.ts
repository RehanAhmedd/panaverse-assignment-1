const favoritePizzas = ['Pepperoni', 'Margherita', 'Supreme'];

// Printing pizza names
for (const pizza of favoritePizzas) {
  console.log(pizza);
}

// Printing a statement for each pizza
for (const pizza of favoritePizzas) {
  console.log(`I like ${pizza} pizza.`);
}

// Printing an additional sentence
console.log(`I really love pizza!. The pizza is delicious`);
